/**
 * Rebuilds the disposable-email blocklist from multiple maintained free
 * sources, merges in our curated entries, and writes BOTH consumers:
 *   - frontend/public/disposable-domains.json  (all_disposable_domains[])
 *   - backend/app/data/disposable_domains.txt   (flat list, if the sibling
 *                                                backend repo is present)
 *
 * Sources (all free, actively maintained):
 *   - 7c/fakefilter            — daily, low false-positive, catches fresh rotations
 *   - disposable-email-domains — long-running, vetted, community-reviewed
 *
 * Run: node scripts/refresh-disposable-list.mjs
 * Safe to run on a schedule. Never adds a known free provider to the blocklist.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const FRONTEND_ROOT = path.resolve(__dirname, "..");
const JSON_PATH = path.join(FRONTEND_ROOT, "public", "disposable-domains.json");
const BACKEND_TXT = path.resolve(
  FRONTEND_ROOT,
  "..",
  "backend",
  "app",
  "data",
  "disposable_domains.txt",
);

const SOURCES = [
  "https://raw.githubusercontent.com/7c/fakefilter/main/txt/data.txt",
  "https://raw.githubusercontent.com/disposable-email-domains/disposable-email-domains/main/disposable_email_blocklist.conf",
];

// Legit free providers must never be flagged disposable, even if a bulk source
// includes one by mistake.
const FREE_PROVIDERS = new Set([
  "gmail.com", "googlemail.com", "outlook.com", "hotmail.com", "live.com",
  "msn.com", "yahoo.com", "ymail.com", "icloud.com", "me.com", "proton.me",
  "protonmail.com", "aol.com", "gmx.com", "gmx.net", "mail.com", "zoho.com",
  "yandex.com", "tutanota.com", "hey.com", "fastmail.com",
]);

const DOMAIN_RE = /^[a-z0-9]([a-z0-9-]{0,61}[a-z0-9])?(\.[a-z0-9]([a-z0-9-]{0,61}[a-z0-9])?)+$/;

function parseDomains(text) {
  const out = [];
  for (let line of text.split(/\r?\n/)) {
    line = line.trim().toLowerCase();
    if (!line || line.startsWith("#") || line.startsWith("//")) continue;
    // some lists use "domain.com # note" or "domain.com,extra"
    line = line.split(/[\s,;]/)[0];
    if (DOMAIN_RE.test(line)) out.push(line);
  }
  return out;
}

async function main() {
  const existing = JSON.parse(fs.readFileSync(JSON_PATH, "utf-8"));

  const domains = new Set();

  // Keep our curated entries + their aliases (block:true only).
  for (const c of existing.curated ?? []) {
    if (c.block === true) {
      domains.add(c.domain.toLowerCase());
      for (const a of c.aliases ?? []) domains.add(a.toLowerCase());
    }
  }
  // Keep whatever was already listed (don't regress).
  for (const d of existing.all_disposable_domains ?? []) domains.add(d.toLowerCase());

  // Merge the fresh sources.
  for (const url of SOURCES) {
    try {
      const res = await fetch(url, { headers: { "User-Agent": "vouchley-refresh" } });
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      const parsed = parseDomains(await res.text());
      parsed.forEach((d) => domains.add(d));
      console.log(`  ${url.split("/").slice(-1)[0]}: +${parsed.length}`);
    } catch (e) {
      console.log(`  WARN: ${url} failed (${e.message}) — keeping existing`);
    }
  }

  // Never flag a legit free provider.
  for (const fp of FREE_PROVIDERS) domains.delete(fp);

  const sorted = [...domains].sort();
  console.log(`Total disposable domains: ${sorted.length}`);
  console.log(`besteya.com present: ${domains.has("besteya.com")}`);

  // Write frontend JSON (preserve structure + metadata).
  existing.all_disposable_domains = sorted;
  existing.version = new Date().toISOString().slice(0, 10);
  if (existing.totals) existing.totals.all_disposable_domains = sorted.length;
  fs.writeFileSync(JSON_PATH, JSON.stringify(existing, null, 2) + "\n");
  console.log(`Wrote ${JSON_PATH}`);

  // Write backend flat list if the sibling repo is present.
  if (fs.existsSync(path.dirname(BACKEND_TXT))) {
    const header = `# Disposable / temporary email domains.\n# Regenerated by frontend/scripts/refresh-disposable-list.mjs from\n# 7c/fakefilter + disposable-email-domains + curated. ${sorted.length} domains.\n`;
    fs.writeFileSync(BACKEND_TXT, header + sorted.join("\n") + "\n");
    console.log(`Wrote ${BACKEND_TXT}`);
  } else {
    console.log(`Backend repo not found at ${BACKEND_TXT} — skipped.`);
  }
}

main();
